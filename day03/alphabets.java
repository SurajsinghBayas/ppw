package day03;

public class alphabets {
    public static void main(String args[]) {
        char a = 'A';
        System.out.println("a to z");
        while (a <= 'Z') {
            System.out.println(a);
            a++;
        }
        char b = 'a';
        System.out.println("A to Z");
        while (b <= 'z') {
            System.out.println(b);
            b++;
        }

    }
}
